package by.htp.less.cycles;

// Необходимо вывести на экран числа от 5 до 1.

public class Task02 {
	public static void main(String[]args) {
		
		 int c = 1; // создание переменной

	        while (c <= 5) {            /// запуск цикла который посчитает 
	        	
	            System.out.println( c);
	            c++;                   // прибавит на одну
	        }

	        System.out.println("Поехали !"); // вывод результата
			
	}    
		
}

