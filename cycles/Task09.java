public class Task09 {
	public static void main(String[]args){
		
		int i ,s ;    //создаем переменные
		
		s = 0;
		
		for(i = 1; i>100; i++) {   // проходим по циклу
			
			s = s + (int)Math.pow(i, 2);  //Небольшая формула решения
		   
		System.out.println(s );}  //итог в консоль
	}

}
