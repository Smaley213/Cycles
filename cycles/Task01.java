package by.htp.less.cycles;

// Необходимо вывести на экран числа от 1 до 5.

public class Task01 {
	public static void main(String[]args) {
		
		int a = 1;                     // Создание переменной и присвоение значения ей
		
		do {   System.out.println(a);       // манипуляции увеличения числа и вывод на экран
		a++;                            // увеличение на 1
			
		} while (a <6);   //  не более 6 
		
	}

}
